package com.ssm.pojo;

public class NewsInfor {
   private int news_id;
   private String news_title;
   private String news_describe;
   private String news_content;
   private String news_time;
   private String news_type;
   private int news_collection;
   private String news_imgUrl;
   private int news_state;

    public int getNews_id() {
        return news_id;
    }

    public void setNews_id(int news_id) {
        this.news_id = news_id;
    }

    public String getNews_title() {
        return news_title;
    }

    public void setNews_title(String news_title) {
        this.news_title = news_title;
    }

    public String getNews_describe() {
        return news_describe;
    }

    public void setNews_describe(String news_describe) {
        this.news_describe = news_describe;
    }

    public String getNews_content() {
        return news_content;
    }

    public void setNews_content(String news_content) {
        this.news_content = news_content;
    }

    public String getNews_time() {
        return news_time;
    }

    public void setNews_time(String news_time) {
        this.news_time = news_time;
    }

    public String getNews_type() {
        return news_type;
    }

    public void setNews_type(String news_type) {
        this.news_type = news_type;
    }

    public int getNews_collection() {
        return news_collection;
    }

    public void setNews_collection(int news_collection) {
        this.news_collection = news_collection;
    }

    public String getNews_imgUrl() {
        return news_imgUrl;
    }

    public void setNews_imgUrl(String news_imgUrl) {
        this.news_imgUrl = news_imgUrl;
    }

    public int getNews_state() {
        return news_state;
    }

    public void setNews_state(int news_state) {
        this.news_state = news_state;
    }
}
