package com.ssm.pojo;

public class PhoneInfor {
    private int phone_id;
    private String phone_model;
    private String phone_img;
    private String phone_price;
    private String phone_type;
    private String phone_infor;
    private String phone_market;
    private int phone_state;

    public int getPhone_id() {
        return phone_id;
    }

    public void setPhone_id(int phone_id) {
        this.phone_id = phone_id;
    }

    public String getPhone_model() {
        return phone_model;
    }

    public void setPhone_model(String phone_model) {
        this.phone_model = phone_model;
    }

    public String getPhone_img() {
        return phone_img;
    }

    public void setPhone_img(String phone_img) {
        this.phone_img = phone_img;
    }

    public String getPhone_price() {
        return phone_price;
    }

    public void setPhone_price(String phone_price) {
        this.phone_price = phone_price;
    }

    public String getPhone_type() {
        return phone_type;
    }

    public void setPhone_type(String phone_type) {
        this.phone_type = phone_type;
    }

    public String getPhone_infor() {
        return phone_infor;
    }

    public void setPhone_infor(String phone_infor) {
        this.phone_infor = phone_infor;
    }

    public String getPhone_market() {
        return phone_market;
    }

    public void setPhone_market(String phone_market) {
        this.phone_market = phone_market;
    }

    public int getPhone_state() {
        return phone_state;
    }

    public void setPhone_state(int phone_state) {
        this.phone_state = phone_state;
    }
}
